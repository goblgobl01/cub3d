#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;
	// int		length;

	// printf("this is s: %s\n", s);
	if (!s)
		return (NULL);
	// length = ft_strlen(s);
	i = 0;
	while (i <= ft_strlen(s))
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i++;
	}
	return (NULL);
}
